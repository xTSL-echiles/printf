#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*b;

	b = (char*)s;
	i = 0;
	while (b[i] != '\0')
		i++;
	if (c == '\0')
		return (&b[i]);
	while (i > 0)
	{
		i--;
		if (b[i] == c)
			return (&b[i]);
	}
	return (0);
}
