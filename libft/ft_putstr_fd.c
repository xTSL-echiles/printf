#include "libft.h"

int		ft_putstr_fd(char *s, int fd)
{
	int i;

	i = 0;
	if (s > 0)
	{
		i = 0;
		while (s[i])
		{
			write(fd, &s[i], 1);
			i++;
		}
	}
	return (i);
}
