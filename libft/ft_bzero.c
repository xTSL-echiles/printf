#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*r;

	r = (unsigned char*)s;
	i = 0;
	while (i < n)
	{
		r[i] = 0;
		i++;
	}
	return (s);
}
